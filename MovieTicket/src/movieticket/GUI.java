    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        String output=null;
        boolean found=false;
        File input=new File("Bills.txt");
        FileReader fr=null;
        Scanner ob=new Scanner(System.in);
        String searchWord,str;
        searchWord = JOptionPane.showInputDialog ("Enter the costomer name you want to search for:");
        try{
    fr=new FileReader (input);
    BufferedReader br=new BufferedReader (fr);
    while ( (str=br.readLine ()) !=null) {
        if (str.contains (searchWord)){
            String[] parts = str.split("\\s+");
            for (int i = 0; i < parts.length; i++) {
                nameTF.setText(parts[0]);
                phoneTF.setText(parts[2]);
                ticketNoTF.setText(parts[5]);
                seatNoTF.setText(parts[7]);
               chargeTF.setText(parts[8]);
                totalPaymentTF.setText(parts[9]);
                 customerCB.setSelectedItem(parts[1]);
                  dayCB.setSelectedItem(parts[3]);
                   timeCB.setSelectedItem(parts[6]);
                if (parts[4].equalsIgnoreCase("Uncharted")) {
                unchartedRB.setSelected (true);
                }
                else if (parts[4].equalsIgnoreCase("Memory")) {
                memoryRB.setSelected (true);
                }
                else if (parts[4].equalsIgnoreCase("Fury")) {
                furyRB.setSelected (true);
                }
                else if (parts[4].equalsIgnoreCase("Infinite")) {
                infiniteRB.setSelected (true);
                }
                else if (parts[4].equalsIgnoreCase("Homefront")) {
                homefrontRB.setSelected (true);
                }               
               else {
               greenlandRB.setSelected (true);}
                
             output=parts[0]+"\t"+"\t"+parts[1]+"\t"+parts[2]+"\t"+parts[3]+"\t"+parts[4]+"\t"+parts[5]+"\t"+"\t"+parts[6]+"\t"+parts[7]+"\t"+"\t"+parts[8]+"\t"+"\t"+parts[9];
                
              
            }
            outputTA.setText("Customer Name\tCustomer Type\tPhone Number\tBooking Day\tMovie\tNumber Of Ticket\tTime\tNumber of seats\tPayment per Ticket\tTotal Payment\n");
            outputTA.append(output);
           found=true;
          
        }
       
    }
    if(found==false){
        JOptionPane.showMessageDialog (null, "The file can't be found",
                "ERROR", JOptionPane. ERROR_MESSAGE); }
             
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog (null, "The file can't be found",
                "ERROR", JOptionPane. ERROR_MESSAGE); 
        } catch (IOException ex) {
            JOptionPane.showMessageDialog (null, "The file can't be found",
                "ERROR", JOptionPane. ERROR_MESSAGE); 
        }
        finally{
            try {
                fr.close ();
            } catch (IOException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    
    }//GEN-LAST:event_searchBTNActionPerformed

