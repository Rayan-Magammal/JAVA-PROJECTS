    private void calculateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateBTNActionPerformed
     try{  
        String bookingDay = (String) dayCB.getSelectedItem ( ) ;
        String customerType= (String) customerCB.getSelectedItem ( ) ;
        int NoOfTicket=Integer.parseInt(ticketNoTF.getText());
        Calculation calculate = new Calculation(bookingDay,  customerType,  NoOfTicket);
        double PaymentPerTicket=calculate.PaymentPerticket(bookingDay, customerType);
        double totalPayment=calculate.totalPaymen(PaymentPerTicket);
        chargeTF.setText("\n" +String.format ("%.2f", PaymentPerTicket));
        totalPaymentTF.setText("\n" +String.format ("%.2f", totalPayment));
    }catch(NumberFormatException e) {
            JOptionPane.showMessageDialog (null, "Please ensure your input is in Interger format!",
                "ERROR", JOptionPane. ERROR_MESSAGE);
        }
        catch(Exception e){
            System.out.print(e.getMessage());
        }

    }//GEN-LAST:event_calculateBTNActionPerformed

    private void saveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTNActionPerformed
     try{  
         String customerName = nameTF.getText ();
     
        String bookMovie = null;
        if (unchartedRB.isSelected()) {
        bookMovie = "Uncharted";
        } 
        else if  (memoryRB.isSelected()){
        bookMovie = "Memory";}
        else if  (furyRB.isSelected()){
        bookMovie = "Fury";}
        else if  (infiniteRB.isSelected()){
        bookMovie = "Infinite";}
        else if  (homefrontRB.isSelected()){
        bookMovie = "Homefront";}
        else{
            bookMovie = "greenland";
        }
        String bookingDay = (String) dayCB.getSelectedItem ( ) ;
        String bookTime = (String) timeCB.getSelectedItem ( ) ;
        String PhoneNumber = phoneTF.getText ();
        String customerType= (String) customerCB.getSelectedItem ( ) ;
        int NoOfTicket=Integer.parseInt(ticketNoTF.getText());
        String numOfSeats=seatNoTF.getText ();
        double payemtPerTicket=Double.parseDouble(chargeTF.getText());
        double totalPayment= Double.parseDouble(totalPaymentTF.getText());
        MovieTicket bill=new MovieTicket( customerName,customerType,PhoneNumber,bookingDay,bookMovie,NoOfTicket,bookTime,numOfSeats,payemtPerTicket,totalPayment);
          billList.add(bill);
          outputTA.setText(bill.toString());
            JOptionPane.showMessageDialog(null, "Data is saved.", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);

            writeFile();
        }catch(NumberFormatException e) {
            JOptionPane.showMessageDialog (null, "Please ensure your input is in Integer format!",
                "ERROR", JOptionPane. ERROR_MESSAGE);
        }
        catch(Exception e){
            System.out.print(e.getMessage());
        
      
        } 
    }//GEN-LAST:event_saveBTNActionPerformed

